#explicit type casting(done by user)

a = '1'
b = '8'
print(a + b)#output = 18
a = int('1')#it'll converse str into int
b = int('8')
print(a + b) #output = 9


#implicit type casting(system will convert it self)

a = 8.00 #float DT
b = 7 #int DT
print(a + b)   #o\p will be in float

